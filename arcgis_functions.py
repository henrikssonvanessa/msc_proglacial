def select_area_shp(area_name, shp_path):
    """
    Extract a single glacier's polygon from the master shapefile.

    Uses an attribute query on 'Glacier_na' to isolate one study area,
    saving it as a standalone shapefile that can be used as a mask in
    subsequent ArcPy spatial operations.

    Parameters
    ----------
    area_name : str  Name of the glacier (must match 'Glacier_na' field)
    shp_path  : str  Path to the master proglacial outlines shapefile

    Returns
    -------
    str  Path to the single-area output shapefile
    """
    area_name_select_shp = f"C:\\TEMP\\Vanessa_Henriksson\\Data\\Python\\Outputs\\{area_name}\\{area_name}_select.shp"
    arcpy.analysis.Select(in_features=shp_path, out_feature_class=area_name_select_shp, where_clause=f"Glacier_na = '{area_name}'")
    return area_name_select_shp


def clip_s2(area_name, s2_reprojected_path, band_name, area_shp):
    """
    Clip a reprojected S2 band to the study area and generate a 10 m fishnet.

    The fishnet is a grid of polygons whose cells exactly align with S2 pixels.
    It is used later as zonal zones when aggregating high-resolution ortho bands
    to 10 m, ensuring spatial alignment between S2 and ortho-derived statistics.

    Parameters
    ----------
    area_name          : str  Glacier name
    s2_reprojected_path: str  Path to the already-reprojected S2 raster
    band_name          : str  S2 band identifier (e.g. 's2_b8')
    area_shp           : str  Path to the single-area mask shapefile

    Returns
    -------
    tuple  (clipped_raster_path, fishnet_shapefile_path)
    """
    _area_name_S2_band_clip_tif = f"C:\\TEMP\\Vanessa_Henriksson\\Data\\Python\\Outputs\\{area_name}\\{area_name}_{band_name}_clip.tif"
    Extract_by_Mask_2_ = _area_name_S2_band_clip_tif
    # ExtractByMask clips to the exact polygon boundary
    _area_name_S2_band_clip_tif = arcpy.sa.ExtractByMask(s2_reprojected_path, area_shp, "INSIDE")
    _area_name_S2_band_clip_tif.save(Extract_by_Mask_2_)

    # Derive fishnet extent from the clipped raster's bounding box
    clip_raster = arcpy.Raster(_area_name_S2_band_clip_tif)
    extent = clip_raster.extent

    # Origin and Y-axis define the coordinate reference for the fishnet
    origin_coord = f"{extent.XMin} {extent.YMin}"
    y_axis_coord = f"{extent.XMin} {extent.YMax}"

    # Create a polygon grid with 10 m x 10 m cells matching S2 pixel footprints
    _area_name_S2_band_fishnet_shp = f"C:\\TEMP\\Vanessa_Henriksson\\Data\\Python\\Outputs\\{area_name}\\{area_name}_{band_name}_fishnet.shp"
    Output_Label_Feature_Class = arcpy.management.CreateFishnet(origin_coord=origin_coord, y_axis_coord=y_axis_coord, out_feature_class=_area_name_S2_band_fishnet_shp, cell_width=10, cell_height=10, number_rows=None, number_columns=None, labels="NO_LABELS", template=_area_name_S2_band_clip_tif, geometry_type="POLYGON")[0]

    return _area_name_S2_band_clip_tif, _area_name_S2_band_fishnet_shp


def reproject_s2(s2_band_path, index, band_name):
    """
    Reproject a Sentinel-2 band to SWEREF99 TM (EPSG:3006).

    S2 imagery is delivered in UTM; this step converts it to the Swedish
    national grid so it aligns with the orthophotos and LiDAR DEM.
    Bilinear resampling preserves continuity of spectral values.

    Parameters
    ----------
    s2_band_path : str  Path to the original S2 raster
    index        : int  Sequential index used to name the output file uniquely
    band_name    : str  S2 band identifier (used in output filename)

    Returns
    -------
    arcpy.Raster  Reprojected raster object
    """
    S2_band = Raster(s2_band_path)
    _area_name_S2_band_reproject_tif = f"C:\\TEMP\\Vanessa_Henriksson\\Data\\Python\\Outputs\\s2_reprojections\\{band_name}_{index}_reproject.tif"
    # Full WKT for SWEREF99 TM ensures exact CRS match with other data sources
    arcpy.management.ProjectRaster(in_raster=S2_band, out_raster=_area_name_S2_band_reproject_tif, out_coor_system="PROJCS[\"SWEREF99_TM\",GEOGCS[\"GCS_SWEREF99\",DATUM[\"D_SWEREF99\",SPHEROID[\"GRS_1980\",6378137.0,298.257222101]],PRIMEM[\"Greenwich\",0.0],UNIT[\"Degree\",0.0174532925199433]],PROJECTION[\"Transverse_Mercator\"],PARAMETER[\"False_Easting\",500000.0],PARAMETER[\"False_Northing\",0.0],PARAMETER[\"Central_Meridian\",15.0],PARAMETER[\"Scale_Factor\",0.9996],PARAMETER[\"Latitude_Of_Origin\",0.0],UNIT[\"Meter\",1.0]]", resampling_type="BILINEAR")
    _area_name_S2_band_reproject_tif = arcpy.Raster(_area_name_S2_band_reproject_tif)
    return _area_name_S2_band_reproject_tif


